package com.mx.totalplay.vo;

public class FechaActualVO {
private int fecha_semana;
private int fecha_mes;
private int fecha_anio;

public int getFecha_semana() {
	return fecha_semana;
}

public void setFecha_semana(int fecha_semana) {
	this.fecha_semana = fecha_semana;
}

public int getFecha_anio() {
	return fecha_anio;
}

public void setFecha_anio(int fecha_anio) {
	this.fecha_anio = fecha_anio;
}

public int getFecha_mes() {
	return fecha_mes;
}

public void setFecha_mes(int fecha_mes) {
	this.fecha_mes = fecha_mes;
}
}
