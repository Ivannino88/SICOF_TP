public class MegasTfeDetallesVO {
	
	private String id_conciliacion;
	private String fecha;
	private String sn;
	private String megas_red;
	private String megas_brm;
	private String accion;
	
	public String getId_conciliacion() {
		return id_conciliacion;
	}
	public void setId_conciliacion(String id_conciliacion) {
		this.id_conciliacion = id_conciliacion;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getSn() {
		return sn;
	}
	public void setSn(String sn) {
		this.sn = sn;
	}
	public String getMegas_red() {
		return megas_red;
	}
	public void setMegas_red(String megas_red) {
		this.megas_red = megas_red;
	}
	public String getMegas_brm() {
		return megas_brm;
	}
	public void setMegas_brm(String megas_brm) {
		this.megas_brm = megas_brm;
	}
	public String getAccion() {
		return accion;
	}
	public void setAccion(String accion) {
		this.accion = accion;
	}
}
