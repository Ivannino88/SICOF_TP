package com.mx.totalplay.vo;

import java.util.Date;

public class BeanGraficaVo {

	private Date fecha;
	private int t_conciliados;
	private int total;
	private String empresa;
	private String semana;
	private String mes;
	private String año;
	private Date fechadg;
	public Date getFecha() {
		return fecha;
	}
	public void setFecha(Date fecha) {
		this.fecha = fecha;
	}
	public int getT_conciliados() {
		return t_conciliados;
	}
	public void setT_conciliados(int t_conciliados) {
		this.t_conciliados = t_conciliados;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public String getSemana() {
		return semana;
	}
	public void setSemana(String semana) {
		this.semana = semana;
	}
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	public String getAño() {
		return año;
	}
	public void setAño(String año) {
		this.año = año;
	}
	public Date getFechadg() {
		return fechadg;
	}
	public void setFechadg(Date fechadg) {
		this.fechadg = fechadg;
	}
}
