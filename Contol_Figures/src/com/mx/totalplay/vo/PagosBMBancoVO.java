package com.mx.totalplay.vo;

public class PagosBMBancoVO {

	private String id;
	private String fecha;
	private String numero_pagos;
	private String importe_brm;
	private String no_pagos_banco;
	private String id_empresa;
	private String empresa;
	private String id_canal;
	private String canal;
	private String importe_bancos;
	private String color;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha.substring(0, 11);
	}
	public String getNumero_pagos() {
		return numero_pagos;
	}
	public void setNumero_pagos(String numero_pagos) {
		this.numero_pagos = numero_pagos;
	}
	public String getImporte_brm() {
		return importe_brm;
	}
	public void setImporte_brm(String importe_brm) {
		this.importe_brm = importe_brm;
	}
	public String getNo_pagos_banco() {
		return no_pagos_banco;
	}
	public void setNo_pagos_banco(String no_pagos_banco) {
		this.no_pagos_banco = no_pagos_banco;
	}
	public String getId_empresa() {
		return id_empresa;
	}
	public void setId_empresa(String id_empresa) {
		this.id_empresa = id_empresa;
	}
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public String getId_canal() {
		return id_canal;
	}
	public void setId_canal(String id_canal) {
		this.id_canal = id_canal;
	}
	public String getCanal() {
		return canal;
	}
	public void setCanal(String canal) {
		this.canal = canal;
	}
	public String getImporte_bancos() {
		return importe_bancos;
	}
	public void setImporte_bancos(String importe_bancos) {
		this.importe_bancos = importe_bancos;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
}
