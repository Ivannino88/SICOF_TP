package com.mx.totalplay.vo;

public class DetalleU2000CanceladasVO {
	
	private String id;
	private String fecha;
	private String cuenta;
	private String plan;
	private String empresa;
	private String fecha_cancelacion;
	private String sn_stb;
	private String tmcode;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha.substring(0, 11);
	}
	public String getCuenta() {
		return cuenta;
	}
	public void setCuenta(String cuenta) {
		this.cuenta = cuenta;
	}
	public String getPlan() {
		return plan;
	}
	public void setPlan(String plan) {
		this.plan = plan;
	}
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public String getFecha_cancelacion() {
		return fecha_cancelacion;
	}
	public void setFecha_cancelacion(String fecha_cancelacion) {
		this.fecha_cancelacion = fecha_cancelacion.substring(0, 11);
	}
	public String getSn_stb() {
		return sn_stb;
	}
	public void setSn_stb(String sn_stb) {
		this.sn_stb = sn_stb;
	}
	public String getTmcode() {
		return tmcode;
	}
	public void setTmcode(String tmcode) {
		this.tmcode = tmcode;
	}
	
	
	

}
