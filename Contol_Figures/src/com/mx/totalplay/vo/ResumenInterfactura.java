package com.mx.totalplay.vo;

public class ResumenInterfactura {
	
	private int no_brm;
	private int TOTAL_EDO_CTA;
	private int TOTAL_FACT;
	private int TOTAL_COMP;
	private int TOTAL_NOTAS;
	private Double TOTAL_FACTURADO;
	
	
	public ResumenInterfactura() {
		super();
	}
	public int getNo_brm() {
		return no_brm;
	}
	public void setNo_brm(int no_brm) {
		this.no_brm = no_brm;
	}
	public int getTOTAL_EDO_CTA() {
		return TOTAL_EDO_CTA;
	}
	public void setTOTAL_EDO_CTA(int tOTAL_EDO_CTA) {
		TOTAL_EDO_CTA = tOTAL_EDO_CTA;
	}
	public int getTOTAL_FACT() {
		return TOTAL_FACT;
	}
	public void setTOTAL_FACT(int tOTAL_FACT) {
		TOTAL_FACT = tOTAL_FACT;
	}
	public int getTOTAL_COMP() {
		return TOTAL_COMP;
	}
	public void setTOTAL_COMP(int tOTAL_COMP) {
		TOTAL_COMP = tOTAL_COMP;
	}
	public int getTOTAL_NOTAS() {
		return TOTAL_NOTAS;
	}
	public void setTOTAL_NOTAS(int tOTAL_NOTAS) {
		TOTAL_NOTAS = tOTAL_NOTAS;
	}
	public Double getTOTAL_FACTURADO() {
		return TOTAL_FACTURADO;
	}
	public void setTOTAL_FACTURADO(Double tOTAL_FACTURADO) {
		TOTAL_FACTURADO = tOTAL_FACTURADO;
	}
	
	
	

}
