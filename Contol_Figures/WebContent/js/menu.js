//funcion para ocultar segun el modulo seleccionado
function clic_MOD(valor){
	var id='#div'+valor.getAttribute("id");
	
if(id!='#divMOD1')
	$('#divMOD1').collapse('hide');
if(id!='#divMOD2')
	$('#divMOD2').collapse('hide');
if(id!='#divMOD3')
	$('#divMOD3').collapse('hide');
if(id!='#divMOD4')
	$('#divMOD4').collapse('hide');
if(id!='#divMOD5')
	$('#divMOD5').collapse('hide');
if(id!='#divMOD6')
	$('#divMOD6').collapse('hide');
if(id!='#divMOD7')
	$('#divMOD7').collapse('hide');
}
